    public EditUserFrame(int id) {
        userservice = new UserService();
        user = userservice.getUserById(id);

        initComponents();

        idTf.setText(String.valueOf(user.getId()));
        nameTf.setText(user.getName());
        phoneTf.setText(user.getPhone());
        userNameTf.setText(user.getUsername());
        passwordField.setText(user.getPassword());
        if ("Admin".equals(user.getRole())) {
            adminRadioBtn.setSelected(true);
        }
        if ("User".equals(user.getRole())) {
            userRadioBtn.setSelected(true);
        }
        String favourites = user.getFavourites();
        if (favourites != null) {
            String[] favs = favourites.split(",");

            for (String f : favs) {
                if (f.equals("Xem phim")) {
                    movieCheckBox.setSelected(true);
                }
                if (f.equals("Choi game")) {
                    gamingCheckBox.setSelected(true);
                }

            }
        }

        aboutTa.setText(user.getAbout());

    }

