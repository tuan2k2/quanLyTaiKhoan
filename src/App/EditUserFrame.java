    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
        // TODO add your handling code here:
        user.setName(nameTf.getText());
        user.setPhone(phoneTf.getText());
        user.setUsername(userNameTf.getText());
        user.setPassword(String.valueOf(passwordField.getPassword()));
        String favourites = "";
        if (adminRadioBtn.isSelected()) {
            user.setRole("Admin");
        }
        if (userRadioBtn.isSelected()) {
            user.setRole("User");
        }
        if (movieCheckBox.isSelected()) {
            favourites += ",Xem phim";
        }
        if (gamingCheckBox.isSelected()) {
            favourites += ",Chơi Game";
        }
        user.setFavourites(favourites);
        user.setAbout(aboutTa.getText());

        userservice.editUser(user);

        new ListUserFrame().setVisible(true);
        this.dispose();
    }//GEN-LAST:event_submitBtnActionPerformed

